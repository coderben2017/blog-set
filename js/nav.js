function goToCover() {
  $('#title').text("随想集");
  $('#content').text("Poetry is a deal of joy and pain and wonder, with a dash of the dictionary..");
  $('#tip').hide();
}

function showContent(id, type) {
  const {title, contents, tips} = type === 'poetry' ? poetries[id]: articles[id];

  let content = '';
  contents.forEach(element => {
    content += `<p>${element}</p>`;
  });

  let tip = "";
  tips.forEach(element => {
    tip += `${element}<br>`;
  });

  $('#title').text(title);
  $('#content').html(content);
  $('#tip').html(tip).show();
  
  if(type === 'article') {
    $('#content').css({
      'text-align': 'left',
      'text-indent': '25px'
    });
  } else {
    $('#content').css({
      'text-align': 'center'
    });
  }
  
  setTimeout(function() {
    if(type === 'poetry') {
      $('#dropdown-1').addClass('open');    
    } else {
      $('#dropdown-2').addClass('open');    
    }
  }, 0);
}

$(function () {
  var trigger = $('.hamburger'),
      overlay = $('.overlay'),
      isClosed = false;

  trigger.click(function () {
    if (isClosed == true) {          
      overlay.hide();
      trigger.removeClass('is-open');
      trigger.addClass('is-closed');
      isClosed = false;
    } else {
      overlay.show();
      trigger.removeClass('is-closed');
      trigger.addClass('is-open');
      isClosed = true;
    }     
  });
  
  $('[data-toggle="offcanvas"]').click(function () {
    $('#wrapper').toggleClass('toggled');
  });

  let poetryMenuHtml = '';
  poetries.forEach((element, index) => {
    if (index !== 26) {
      poetryMenuHtml += `
      <li>
        <a href=\"javascript:;\" onclick=\"showContent(${index}, 'poetry')\" style=\"background-color: #333\">
          ${element.title}
        </a>
      </li>
      `;
    }
  });
  $('#menu-poetry').html(poetryMenuHtml);

  let articleMenuHtml = '';
  articles.forEach((element, index) => {
    articleMenuHtml += `
    <li>
      <a href=\"javascript:;\" onclick=\"showContent(${index}, 'article')\" style=\"background-color: #333\">
        ${element.title}
      </a>
    </li>
    `;
  });
  $('#menu-article').html(articleMenuHtml);

  goToCover();
});


